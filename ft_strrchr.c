char	*ft_strrchr(const char *s, int c)
{
	int	n;

	n = 0;
	while (s[n] != '\0')
		n++;
	while (s[n] != c && n <= 0)
		n--;
	if (n == 0)
		return (NULL);
	else
		return (&s[n]);
}
